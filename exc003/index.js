// Arrays
let heroes = ["Super Man", "Batman", "Thor"]
console.log(heroes)

// indexOF
let thorIndex = heroes.indexOf('Thor')
console.log(heroes[thorIndex])

// Join
let heroesJoin = heroes.join(' | ')
console.log(heroesJoin)

// Concat
let heroesConcant = heroes.concat(['Aquaman', 'Capitan america'])
console.log(heroesConcant)

// Push
let pushHeroes = heroes.push('Aquaman', 'Soldier Boy')
console.log(heroes)

// Pop
let popHeroes = heroes.pop()
console.log(heroes)
console.log(popHeroes)